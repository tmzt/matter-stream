//! FNTa — Font Atlas binary format.
//!
//! A compact bitmap font atlas for monospace glyph rendering.
//!
//! ## Wire format
//!
//! ```text
//! [4 bytes]  magic: "FNTa"
//! [1 byte]   version (1)
//! [1 byte]   glyph_w — pixels per row
//! [1 byte]   glyph_h — rows per glyph
//! [1 byte]   first_cp — first codepoint (typically 0x20)
//! [1 byte]   last_cp — last codepoint (typically 0x7E)
//! [1 byte]   reserved (0)
//! [N bytes]  bitmap data — glyph_h bytes per glyph, 1 row per byte,
//!            pixels packed MSB-first (bit glyph_w-1 .. bit 0)
//! ```

use std::fmt;

pub const FNTA_MAGIC: &[u8; 4] = b"FNTa";
pub const FNTA_VERSION: u8 = 1;
pub const FNTA_HEADER_SIZE: usize = 10;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum FntaError {
    BadMagic,
    UnsupportedVersion(u8),
    TruncatedData,
}

impl fmt::Display for FntaError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            FntaError::BadMagic => write!(f, "invalid FNTa magic bytes"),
            FntaError::UnsupportedVersion(v) => write!(f, "unsupported FNTa version: {}", v),
            FntaError::TruncatedData => write!(f, "truncated FNTa data"),
        }
    }
}

/// A decoded bitmap font atlas.
#[derive(Debug, Clone)]
pub struct FontAtlas {
    pub glyph_w: u8,
    pub glyph_h: u8,
    pub first_cp: u8,
    pub last_cp: u8,
    pub bitmap: Vec<u8>,
}

impl FontAtlas {
    /// Decode a font atlas from FNTa binary data.
    pub fn decode(data: &[u8]) -> Result<Self, FntaError> {
        if data.len() < FNTA_HEADER_SIZE {
            return Err(FntaError::TruncatedData);
        }
        if &data[0..4] != FNTA_MAGIC {
            return Err(FntaError::BadMagic);
        }
        let version = data[4];
        if version != FNTA_VERSION {
            return Err(FntaError::UnsupportedVersion(version));
        }
        let glyph_w = data[5];
        let glyph_h = data[6];
        let first_cp = data[7];
        let last_cp = data[8];
        // data[9] = reserved

        let glyph_count = (last_cp as usize).saturating_sub(first_cp as usize) + 1;
        let expected_bitmap_len = glyph_count * glyph_h as usize;
        if data.len() < FNTA_HEADER_SIZE + expected_bitmap_len {
            return Err(FntaError::TruncatedData);
        }

        let bitmap = data[FNTA_HEADER_SIZE..FNTA_HEADER_SIZE + expected_bitmap_len].to_vec();
        Ok(Self { glyph_w, glyph_h, first_cp, last_cp, bitmap })
    }

    /// Encode to FNTa binary format.
    pub fn encode(&self) -> Vec<u8> {
        let mut buf = Vec::with_capacity(FNTA_HEADER_SIZE + self.bitmap.len());
        buf.extend_from_slice(FNTA_MAGIC);
        buf.push(FNTA_VERSION);
        buf.push(self.glyph_w);
        buf.push(self.glyph_h);
        buf.push(self.first_cp);
        buf.push(self.last_cp);
        buf.push(0); // reserved
        buf.extend_from_slice(&self.bitmap);
        buf
    }

    /// Number of glyphs in this atlas.
    pub fn glyph_count(&self) -> usize {
        (self.last_cp as usize).saturating_sub(self.first_cp as usize) + 1
    }

    /// Get the bitmap rows for a glyph. Returns `glyph_h` bytes, one per row.
    /// Each row byte has pixels packed left-to-right in bits (glyph_w-1)..0.
    /// Returns None for codepoints outside the atlas range.
    pub fn glyph_rows(&self, codepoint: u8) -> Option<&[u8]> {
        if codepoint < self.first_cp || codepoint > self.last_cp {
            return None;
        }
        let idx = (codepoint - self.first_cp) as usize;
        let h = self.glyph_h as usize;
        let start = idx * h;
        if start + h > self.bitmap.len() {
            return None;
        }
        Some(&self.bitmap[start..start + h])
    }

    /// Test whether a pixel is set in a glyph.
    /// `row` is 0..glyph_h, `col` is 0..glyph_w (0 = leftmost).
    pub fn pixel_set(&self, codepoint: u8, row: u8, col: u8) -> bool {
        if col >= self.glyph_w {
            return false;
        }
        if let Some(rows) = self.glyph_rows(codepoint) {
            if (row as usize) < rows.len() {
                // bit (glyph_w - 1 - col) — MSB = leftmost
                let bit = self.glyph_w - 1 - col;
                rows[row as usize] & (1 << bit) != 0
            } else {
                false
            }
        } else {
            false
        }
    }
}

