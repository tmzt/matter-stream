/// Return the built-in 5×8 monospace bitmap font atlas.
///
/// Glyph encoding: 8 rows per character, 5 pixels per row.
/// Bit layout per row byte: bit4 = leftmost pixel, bit0 = rightmost.
pub fn builtin_font() -> FontAtlas {
    FontAtlas {
        glyph_w: 5,
        glyph_h: 8,
        first_cp: 0x20,
        last_cp: 0x7E,
        bitmap: BUILTIN_5X8.to_vec(),
    }
}

