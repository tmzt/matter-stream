//! matterstream-processor
//!
//! This crate defines the `Processor` responsible for taking the compiled output
//! from `matterstream-compiler` and further processing it. This includes
//! resolving TSX imports to Rust-implemented primitives and expanding functional components
//! into an intermediate representation suitable for execution or rendering.

use anyhow::{Result, anyhow};
use dashmap::DashMap;
use matterstream_core::{MtsmObject, MtsmExecFunctionalComponent, TsxFragment, Op, CompiledOps};
use matterstream_packages::PackageRegistry;

/// A type alias for `Result` with `ProcessorError` as the error type.
pub type ProcessorResult<T> = Result<T>;
/// The error type for the `matterstream-processor` crate, utilizing `anyhow::Error`.
pub type ProcessorError = anyhow::Error;

/// Represents the output of the processor after resolving imports and expanding functional components.
///
/// This structure holds the transformed UI representation ready for the renderer,
/// including a root UI fragment and any associated MatterStream data.
pub struct ProcessorOutput {
    /// The root `TsxFragment` representing the processed UI structure.
    pub root_fragment: TsxFragment,
    /// Any associated MatterStream objects, bindings, or other data.
    pub mtsm_data: MtsmObject,
    /// A collection of executable operations generated by the processor.
    pub ops: ProcessorOutputOps,
}

/// A collection of executable operations generated by the processor.
///
/// This encapsulates the low-level rendering or execution commands.
pub struct ProcessorOutputOps {
    /// A vector of core MatterStream operations.
    pub ops: Vec<Op>,
    // pub executable_components: Vec<Box<dyn MtsmExecFunctionalComponent>>, // Could also store exec components here
}

/// The main processor for MatterStream UI definitions.
///
/// This component takes `CompiledOps` from the compiler, resolves imports
/// using a `PackageRegistry`, and transforms the AST into `ProcessorOutput`
/// containing executable components and operations.
pub struct Processor;

